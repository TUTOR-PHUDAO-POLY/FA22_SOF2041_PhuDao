    // Hàm này dùng để đổ dữ liệu Chức vụ lên JTable
    private void loadTable() {
        // Lấy danh sách chức vụ
        // Liên quan đến chức vụ thì tìm ChucVuService để lấy
        ArrayList<ChucVuViewModel> dsChucVu = chucVuService.layDanhSachChucVu();
        
        // Model <=> Đối tượng chuyên dùng lưu dữ liệu
        //      => TableModel <=> Đối tượng chứa dữ liệu của Table
        //      => DefaultTableModel <=> Đối tượng mặc định chứa dữ liệu của Table
        // => Toàn bộ dữ liệu của JTable sẽ nằm trong TableModel
        DefaultTableModel tableModel = (DefaultTableModel) tbChucVu.getModel();
        
        // Chỉnh lại cột của Table
        tableModel.setColumnCount(0); // Xóa các cột cũ
        tableModel.addColumn("Id"); // Thêm cột mới
        tableModel.addColumn("Mã + Tên");
        
        // Đổ dữ liệu vào các hàng/dòng của Table
        tableModel.setRowCount(0); // Xóa các hàng/dòng cũ
        // Duyệt danh sách chức vụ để thêm từng chức vụ vào Table
        for(ChucVuViewModel chucVu : dsChucVu) {
            // Tạo hàng/dòng
            Object[] row = new Object[] {
                chucVu.getId(),
                chucVu.getMaTen(),
            };
            
            // Thêm hàng/dòng vừa tạo vào Table
            tableModel.addRow(row);
        }
    }

